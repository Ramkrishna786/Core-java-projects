package MethodsImplemention;

public class StudentMarks extends Student
{	
	private int studentMarksEnglish;
	private int studentMarksMaths;
	private int studentMarksScience;
	
	//generating getter and setter methods
	public int getStudentMarksEnglish() {
		return studentMarksEnglish;
	}
	public void setStudentMarksEnglish(int studentMarksEnglish) {
		this.studentMarksEnglish = studentMarksEnglish;
	}
	public int getStudentMarksMaths() {
		return studentMarksMaths;
	}
	public void setStudentMarksMaths(int studentMarksMaths) {
		this.studentMarksMaths = studentMarksMaths;
	}
	public int getStudentMarksScience() {
		return studentMarksScience;
	}
	public void setStudentMarksScience(int studentMarksScience) {
		this.studentMarksScience = studentMarksScience;
	}
	
	
}

package MethodsImplemention;

public class FullTimeEmployee extends EmployeeSecond
{
	int fullTimeSalary;
	int fullTimeBonus;
	
	//generating getter and setter methods
	public int getFullTimeSalary() {
		return fullTimeSalary;
	}
	public void setFullTimeSalary(int fullTimeSalary) {
		this.fullTimeSalary = fullTimeSalary;
	}
	public int getFullTimeBonus() {
		return fullTimeBonus;
	}
	public void setFullTimeBonus(int fullTimeBonus) {
		this.fullTimeBonus = fullTimeBonus;
	}
	
	

}

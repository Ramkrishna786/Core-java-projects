package MethodsImplemention;

public class PartTimeEmployee extends EmployeeSecond
{
	int partTimeRate;
	int partTimeHour;
	
	//generating getter and setter methods
	public int getPartTimeRate() {
		return partTimeRate;
	}
	public void setPartTimeRate(int partTimeRate) {
		this.partTimeRate = partTimeRate;
	}
	public int getPartTimeHour() {
		return partTimeHour;
	}
	public void setPartTimeHour(int partTimeHour) {
		this.partTimeHour = partTimeHour;
	}
	

}
